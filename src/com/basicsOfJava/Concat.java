package com.basicsOfJava;

public class Concat {


    public static void main(String[] args) {

        int x=10;
        int y=20;
        String str1="Hello";
        String str2="World";
        System.out.println(x+y);
        System.out.println(str1+str2);
        System.out.println(x+y+str1+str2);
        System.out.println(str1+str2+x+y);
        System.out.println(str1+str2+(x+y));



    }
}
